// switch case statement

// break manages the control of the flow, if there is no break then it executes the remaining code except "default case"


const month = "Mar"

switch (month) {
    case "Jan":
        console.log("January");
        break;
    case "Feb":
        console.log("February");
        break;
    case "Mar":
        console.log("March");
        break;
    case "Apr":
        console.log("April");
        break;
        
        
    default:
        console.log("default case match");
        break;
}





