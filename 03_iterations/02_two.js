// while loop

let index = 0
while (index <= 10){
    console.log(`Value of index is ${index}`);
    index = index + 2
}

let realID = ["Wade", "Bruce", "Peter", "Clark"]

let arr = 0
while (arr < realID.length){
    console.log(`Value is ${realID[arr]}`);
    arr = arr + 1
}

// do-while loop
// In this loop, the execution is done before checking the condition

// let score = 1
let score = 11

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);